        .distinctBy { it.englishWord.trim().lowercase() }
        .map { entry ->
            WordEntity(
                englishWord = entry.englishWord,
                indonesianMeaning = entry.indonesianMeaning,
                category = entry.category,
                exampleSentence = exampleSentenceFor(entry.category, entry.englishWord)
            )
        }

    private data class Entry(
        val englishWord: String,
        val indonesianMeaning: String,
        val category: String
    )

    private fun category(category: String, vararg pairs: String): List<Entry> {
        return pairs.map { pair ->
            val parts = pair.split("|", limit = 2)
            require(parts.size == 2) { "Vocabulary pair must use english|meaning: $pair" }
            Entry(
                englishWord = parts[0].trim(),
                indonesianMeaning = parts[1].trim(),
                category = category
            )
        }
    }

    private fun exampleSentenceFor(category: String, word: String): String {
        val lowerWord = word.lowercase()
        return when (category) {
            "Action" -> "I can $lowerWord every day."
            "Adjective" -> "It is $lowerWord."
            "Body" -> "This is my $lowerWord."
            "Clothing" -> "I wear ${articlePhrase(lowerWord)}."
            "Color" -> "My favorite color is $lowerWord."
            "Drink" -> "I drink $lowerWord."
            "Family" -> "This is my $lowerWord."
            "Feeling" -> "I feel $lowerWord."
            "Food", "Fruit", "Vegetable" -> "I like $lowerWord."
            "Home" -> "This is ${articlePhrase(lowerWord)} at home."
            "Job" -> "${articlePhrase(lowerWord).replaceFirstChar { it.uppercase() }} helps people."
            "Nature" -> "I can see the $lowerWord."
            "Number" -> "I can count to $lowerWord."
            "Object" -> "This is ${articlePhrase(lowerWord)}."
            "Place" -> "I go to the $lowerWord."
            "Position" -> "The word $lowerWord shows position."
            "School" -> "I see ${articlePhrase(lowerWord)} at school."
            "Time" -> "The word $lowerWord tells time."
            "Transportation" -> "I ride ${articlePhrase(lowerWord)}."
            "Weather" -> "The weather is $lowerWord today."
            "Expression" -> "I can use $lowerWord when I speak."
            else -> "I learn the word $lowerWord."
        }
    }

    private fun articlePhrase(word: String): String {
        val pluralWords = setOf(
            "children",
            "clothes",
            "feet",
            "glasses",
            "gloves",
            "noodles",
            "pants",
            "parents",
            "pajamas",
            "roller skates",
            "sandals",
            "scissors",
            "shoes",
            "shorts",
            "slippers",
            "socks",
            "stairs",
            "teeth",
            "twins"
        )
        if (word in pluralWords) return "some $word"

        val article = when (word.firstOrNull()) {
            'a', 'e', 'i', 'o', 'u' -> "an"
            else -> "a"
        }
        return "$article $word"
    }
